#include <iostream>

int main()
{
    // 1 Byte
    bool my_bool1 = true;
    bool my_bool2 = false;

    // 1 Byte = 8 Bit (256)
    char my_char = 10;

    // 2 Byte = 16 Bit (65.000)
    short my_short = 300;

    // 4 Byte = 32 Bit = 4.000.000.000
    int my_int = 100000;

    // 8 Byte = 64 Bit
    long long my_ll = 5000000000;

    // 4 Byte = 32 Bit
    float my_float = 10.0;

    double my_dbl = 10.0;



    return 0;
}
